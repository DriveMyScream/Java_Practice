package String;
import java.util.*;

public class Operators_in_String {
    public static void main(String[] args) {
        System.out.println('A' + 'B');
        System.out.println('a' + 'b');
        System.out.println((char)('a' + 3));
        System.out.println("A" + "B");
        System.out.println("a" + "b");
        System.out.println("A" + 10);
        System.out.println("A" + new ArrayList<>());
    }
}
